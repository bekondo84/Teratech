    /**
     *
     * @param currentObject
     * @throws IllegalAccessException
     * @throws IllegalArgumentException
     */
    public void setCurrentObject(T currentObject) throws IllegalArgumentException, IllegalAccessException {
        this.currentObject = currentObject;

        boolean status = beforeSetCurrentObject();

        if (!status) {
            return;
        }

        if (getFiledsPanel() == null) {
            return;
        }

        if (currentObject != null && getFiledsPanel() != null) {
            ValidateAndFillBeans.fillViewBean(currentObject, getFiledsPanel());
        }

        if (typeOperation == TypeOperation.UPDATE) {
            ValidateAndFillBeans.updatableAnnotationProcessor(getFiledsPanel());
        } else if (typeOperation == TypeOperation.VIEW) {
            ValidateAndFillBeans.disableAllFields(getFiledsPanel());
        }

        //Action apres mise a jour
        afterSetCurrentObject();
    }

    /**
     * Suppression des donnÃ©es du SI
     */
    public void delete() {
        try {
            if (currentObject == null) {
                return;
            }

            boolean status = beforeDelete();

            if (!status) {
                return;
            }

            getManager().delete(getPrimaryKey(currentObject));

            afterDelete();

            if (getParentFrame() != null) {
                getParentFrame().search();
            }

            this.dispose();
        } catch (Exception ex) {
            CustomLogger.loggerWriteError(getClass(), "nouveau()", MessagesBundle.getMessage("new.entity.error"));
        }
    }

