    /**
     * Recupere les valeurs et construit des objects
     *
     * @throws IllegalAccessException
     */
    public void collecteCurrentObjectData() throws IllegalAccessException {
        if (getFiledsPanel() == null) {
            return;
        }
        //Validation et transfert du contenu
        if (ValidateAndFillBeans.validateBean(getFiledsPanel())) {
            currentObject = ValidateAndFillBeans.fillBean(getFiledsPanel(), currentObject);
        }
        //System.out.println("Current Usr Account :::::: "+currentObject);
    }

    /**
     *
     */
    public void save() {

        try {
            //Initialisation des champs du currentOject
            collecteCurrentObjectData();
            //Verification si il ya une entite
            if (currentObject == null) {
                return;
            }
            //Verification de l'existance d'un manager
            if (getManager() == null && model == null) {
                return;
            }

            //Verification de la prÃ©condition		
            boolean precondition = beforeSave();

            if (!precondition) {
                return;
            }

            if (getPrimaryKey(currentObject) == null) {
                CustomLogger.loggerWriteError(getClass(), "save()", MessagesBundle.getMessage("new.entity.id.null"));
                Notification.getNotificationDialog(getApplicationFrame(), true, NotificationType.ERROR, "Erreur pendant  le traitement\nVeuillez consulter les détails", "L'identifiant ne peut être null");
            }

            //Sauvegarde de currentObject 
            if (typeOperation == TypeOperation.NEW) {
                //Test d'existance
                if (!interne && getManager().find(AnnotationsProcessor.getPrimaryKeyName(currentObject.getClass()), getPrimaryKey(currentObject)) != null) {
                    CustomLogger.loggerWriteError(getClass(), "nouveau()", MessagesBundle.getMessage("new.entity.error"));
                    Notification.getNotificationDialog(getApplicationFrame(), true, NotificationType.ERROR, "Duplicate key entry", "Un enregistrement de même cle existe déja");
                    return;
                }
                //Traitement de notre enregistrement
                if (!interne) {
                    getManager().save(currentObject);
                } else {
                    //System.out.println("AbstractEditTemplateDialog.save() :::::::::::::::::::::::::::::::::::: "+currentObject+"  :::::::::::::::::::::::::::::::: "+model.exist((Comparable) currentObject)+" ::::::: "+model.getElements());
                    if (!model.exist((Comparable) currentObject)) {
                        model.addElement((Comparable) currentObject);
                    } else {
                        CustomLogger.loggerWriteError(getClass(), "nouveau()", MessagesBundle.getMessage("new.entity.error"));
                        Notification.getNotificationDialog(getApplicationFrame(), true, NotificationType.ERROR, "Duplicate key entry", "Un enregistrement de même clé existe déja");
                        return;
                    }//if(!model.getElements().contains(currentObject)){
                }
            } else if (typeOperation == TypeOperation.UPDATE) {
                if (!interne) {
                    getManager().update(getPrimaryKey(currentObject), currentObject);
                } else {
                    model.updateElement(selectedRow, (Comparable) currentObject);
                }
            }
            //Validation de la post condition
            afterSave();
            //Mise a jour de la fenÃªtre parent si elle existe
            if (getParentFrame() != null) {
                getParentFrame().search();
            }
            this.dispose();
        } catch (Exception ex) {ex.printStackTrace();
            CustomLogger.loggerWriteError(getClass(), "nouveau()", MessagesBundle.getMessage("new.entity.error"));
            Notification.getNotificationDialog(getApplicationFrame(), true, NotificationType.ERROR, "Erreur pendant  le traitement\nVeuillez consulter les détails", ex.getMessage());

        }
    }

